package Test.ArrayTest;

public class ClassArrayList {
    private String name;
    private int age;

    public ClassArrayList() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public ClassArrayList(String name, int age) {

        this.name = name;
        this.age = age;

    }
}
